"""Trundlebot dance example"""

# Import the trundle library
# You need to do this before you call any trundle commands
import trundle

# Loop through these commands indefinitely
while True:
    trundle.forward(20)
    trundle.back(20)
    trundle.left(25)
    trundle.forward(20)
    trundle.back(20)
    trundle.right(50)
    trundle.forward(20)
    trundle.back(20)
    trundle.left(25)
    trundle.forward(20)
    trundle.back(20)
    trundle.back(20)
    trundle.forward(20)

